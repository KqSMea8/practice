package cn.com.upcard.mgateway.controller.response;

public class TradecloseResponse extends BaseResponse {
	private static final long serialVersionUID = 3712032233883820266L;
	public TradecloseResponse() {
		super();
	}
	public TradecloseResponse(String code, String msg) {
		super(code, msg);
	}
	private String mchId;// 商户号
	private String outTradeNo;
	private String tradeState;
	public String getMchId() {
		return mchId;
	}
	public void setMchId(String mchId) {
		this.mchId = mchId;
	}
	public String getOutTradeNo() {
		return outTradeNo;
	}
	public void setOutTradeNo(String outTradeNo) {
		this.outTradeNo = outTradeNo;
	}
	public String getTradeState() {
		return tradeState;
	}
	public void setTradeState(String tradeState) {
		this.tradeState = tradeState;
	}
	
}
