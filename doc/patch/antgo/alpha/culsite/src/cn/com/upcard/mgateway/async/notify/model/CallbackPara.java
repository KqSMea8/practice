package cn.com.upcard.mgateway.async.notify.model;

import java.util.Map;

public class CallbackPara {

	private String url; // 推送的url
	private String orderId; // 交易表的id
	private Map<String, String> contentMap; // 发送内容

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public Map<String, String> getContentMap() {
		return contentMap;
	}

	public void setContentMap(Map<String, String> contentMap) {
		this.contentMap = contentMap;
	}

	@Override
	public String toString() {
		return "CallbackPara [url=" + url + ", orderId=" + orderId + ", contentMap=" + contentMap + "]";
	}

}
