package cn.com.upcard.mgateway.channel.bank.lkl;

public enum RevokType {
	/**
	 * <pre>
	 * 用户发起的撤销操作
	 * </pre>
	 */
	USER("01"),
	/**
	 * <pre>
	 * POS端轮询超时发起的撤销
	 * </pre>
	 */
	POS("02");
	
	private String revokCode;
	
	public String getRevokCode() {
		return revokCode;
	}

	private RevokType(String revokCode) {
		this.revokCode = revokCode;
	}
}
