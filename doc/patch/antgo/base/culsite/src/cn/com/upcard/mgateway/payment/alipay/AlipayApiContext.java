package cn.com.upcard.mgateway.payment.alipay;

public class AlipayApiContext {
	private String gateway;
	private String charset;
	private String signType;
	private String version;
	private String timestamp;
	private String appId;
	private String privateKey;
	private String publicKey;
	private String bizPublicKey;
	
	public String getGateway() {
		return gateway;
	}
	public void setGateway(String gateway) {
		this.gateway = gateway;
	}
	public String getCharset() {
		return charset;
	}
	public void setCharset(String charset) {
		this.charset = charset;
	}
	public String getSignType() {
		return signType;
	}
	public void setSignType(String signType) {
		this.signType = signType;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public String getPrivateKey() {
		return privateKey;
	}
	public void setPrivateKey(String privateKey) {
		this.privateKey = privateKey;
	}
	public String getPublicKey() {
		return publicKey;
	}
	public void setPublicKey(String publicKey) {
		this.publicKey = publicKey;
	}
	public String getBizPublicKey() {
		return bizPublicKey;
	}
	public void setBizPublicKey(String bizPublicKey) {
		this.bizPublicKey = bizPublicKey;
	}
}
