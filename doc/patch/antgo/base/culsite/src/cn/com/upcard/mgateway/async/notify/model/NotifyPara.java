package cn.com.upcard.mgateway.async.notify.model;

import java.util.Map;

/**
 * 消息推送参数bean
 * 
 * @author zhoudi
 * 
 */
public class NotifyPara {

	private String url; // 目标url
	private Map<String, String> contentMap; // 推送的内容
	private String orderId; // 订单表主键
	private String status; // 推送状态
	private String pushTime; // 本次推送的时间
	private int pushedNumber; // 已推送的次数

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public Map<String, String> getContentMap() {
		return contentMap;
	}

	public void setContentMap(Map<String, String> contentMap) {
		this.contentMap = contentMap;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getPushTime() {
		return pushTime;
	}

	public void setPushTime(String pushTime) {
		this.pushTime = pushTime;
	}

	public int getPushedNumber() {
		return pushedNumber;
	}

	public void setPushedNumber(int pushedNumber) {
		this.pushedNumber = pushedNumber;
	}

}
